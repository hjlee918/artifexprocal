/// Compute XYZ tristimulus values by integrating a spectral power distribution
/// against the CIE 1931 2° observer color matching functions.
///
/// `spectrum` must be a SPD sampled at 5 nm intervals from 360–830 nm
/// (same length as `CIE_WAVELENGTHS_NM`).
pub fn integrate_spd_to_xyz(spectrum: &[f64]) -> crate::types::Xyz {
    assert_eq!(spectrum.len(), CIE_1931_2DEG_OBSERVER.len());

    let mut x = 0.0;
    let mut y = 0.0;
    let mut z = 0.0;
    let step = 5.0; // nm

    for i in 0..spectrum.len() {
        let (xb, yb, zb) = CIE_1931_2DEG_OBSERVER[i];
        let val = spectrum[i];
        x += xb * val * step;
        y += yb * val * step;
        z += zb * val * step;
    }

    let k = 100.0 / y;
    crate::types::Xyz { x: x * k, y: y * k, z: z * k }
}

