#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_spectral_locus_count() {
        assert_eq!(SPECTRAL_LOCUS_1931.len(), 81);
    }

    #[test]
    fn test_spectral_locus_sanity() {
        for &(x, y) in SPECTRAL_LOCUS_1931 {
            assert!(x > 0.0 && y > 0.0, "coordinates must be positive");
            assert!(x + y <= 1.0, "sum must be less than or equal to 1");
        }
    }

    #[test]
    fn test_xy_to_uprime_vprime_d65() {
        let (u, v) = xy_to_uprime_vprime(0.3127, 0.3290);
        assert!((u - 0.1978).abs() < 0.001, "D65 u' should be ~0.1978");
        assert!((v - 0.4683).abs() < 0.001, "D65 v' should be ~0.4683");
    }

    #[test]
    fn test_spectral_locus_1976_spot_check() {
        let locus = spectral_locus_1976();
        assert_eq!(locus.len(), 81);
        // 520 nm: uv should be (~0.023, ~0.584)
        let (u, v) = locus[28]; // index 28 = 520 nm
        assert!((u - 0.0231).abs() < 0.001, "520 nm u' should be ~0.023");
        assert!((v - 0.5837).abs() < 0.001, "520 nm v' should be ~0.584");
    }

    #[test]
    fn test_spectral_locus_1976_matches_computed() {
        for i in 0..SPECTRAL_LOCUS_1931.len() {
            let (x, y) = SPECTRAL_LOCUS_1931[i];
            let (u, v) = xy_to_uprime_vprime(x, y);
            let (eu, ev) = SPECTRAL_LOCUS_1976[i];
            assert!((u - eu).abs() < 0.0001, "u' mismatch at index {} ({} nm)", i, 380 + i * 5);
            assert!((v - ev).abs() < 0.0001, "v' mismatch at index {} ({} nm)", i, 380 + i * 5);
        }
    }
}
