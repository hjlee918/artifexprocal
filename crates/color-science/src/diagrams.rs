/// Convert CIE 1931 xy to CIE 1976 u'v'
pub fn xy_to_uprime_vprime(x: f64, y: f64) -> (f64, f64) {
    let denom = -2.0 * x + 12.0 * y + 3.0;
    if denom.abs() < f64::EPSILON {
        return (0.0, 0.0);
    }
    let u_prime = (4.0 * x) / denom;
    let v_prime = (9.0 * y) / denom;
    (u_prime, v_prime)
}

/// Return CIE 1976 u'v' spectral locus
pub fn spectral_locus_1976() -> Vec<(f64, f64)> {
    SPECTRAL_LOCUS_1931.iter().map(|&(x, y)| xy_to_uprime_vprime(x, y)).collect()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_spectral_locus_count() {
        assert_eq!(SPECTRAL_LOCUS_1931.len(), 81);
    }

    #[test]
    fn test_spectral_locus_sanity() {
        for &(x, y) in SPECTRAL_LOCUS_1931 {
            assert!(x > 0.0 && y > 0.0, "coordinates must be positive");
            assert!(x + y < 1.0, "sum must be less than 1");
        }
    }

    #[test]
    fn test_xy_to_uprime_vprime_d65() {
        let (u, v) = xy_to_uprime_vprime(0.3127, 0.3290);
        assert!((u - 0.1978).abs() < 0.001, "D65 u' should be ~0.1978");
        assert!((v - 0.4683).abs() < 0.001, "D65 v' should be ~0.4683");
    }

    #[test]
    fn test_spectral_locus_1976_count() {
        let locus = spectral_locus_1976();
        assert_eq!(locus.len(), 81);
    }
}
