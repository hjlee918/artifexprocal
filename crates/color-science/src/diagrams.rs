/// Convert CIE 1931 xy to CIE 1976 u'v'
pub fn xy_to_uv(x: f64, y: f64) -> (f64, f64) {
    let denom = -2.0 * x + 12.0 * y + 3.0;
    if denom == 0.0 {
        return (0.0, 0.0);
    }
    let u_prime = (4.0 * x) / denom;
    let v_prime = (9.0 * y) / denom;
    (u_prime, v_prime)
}

/// Return CIE 1976 u'v' spectral locus
pub fn spectral_locus_1976() -> Vec<(f64, f64)> {
    SPECTRAL_LOCUS_1931.iter().map(|&(x, y)| xy_to_uv(x, y)).collect()
}
